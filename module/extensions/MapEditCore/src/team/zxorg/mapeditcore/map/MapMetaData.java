package team.zxorg.mapeditcore.map;

public class MapMetaData {
    /**
     * 歌曲标题
     */
    String title;
    /**
     * 歌曲标题unicode(通常为罗马音)
     */
    String titleUnicode;
    /**
     * 艺术家
     */
    String artist;
    /**
     * 艺术家unicode(通常为罗马音)
     */
    String artistUnicode;
    /**
     * 图作者
     */
    String creator;
    /**
     * 难度名(版本名)
     */
    String version;
    /**
     * 歌曲信息与档案的来源
     */
    String source;
    /**
     * 空格分隔的 String数组,易于搜索的标签
     */
    String Tags;


}
